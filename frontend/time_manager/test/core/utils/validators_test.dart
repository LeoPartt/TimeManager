import 'package:flutter_test/flutter_test.dart';
import 'package:time_manager/core/utils/validators.dart';

void main() {
  group('Validators', () {
    test('validateEmail returns error when empty', () {
      final result = Validators.validateEmail('');
      expect(result, "Email address is required");
    });

    test('validateEmail returns error for invalid email', () {
      final result = Validators.validateEmail('notanemail');
      expect(result, "Invalid email address");
    });

    test('validateEmail returns null for valid email', () {
      final result = Validators.validateEmail('test@example.com');
      expect(result, null);
    });

    test('validatePassword returns error when empty', () {
      final result = Validators.validatePassword('');
      expect(result, "Password is required");
    });

    test('validatePassword returns error for short password', () {
      final result = Validators.validatePassword('123');
      expect(result, contains('6 characters'));
    });

    test('validatePassword returns null for strong password', () {
      final result = Validators.validatePassword('123456');
      expect(result, null);
    });

    test('validateNotEmpty returns error if empty', () {
      final String name = 'Name';
      final result = Validators.validateNotEmpty('', name);
      expect(result, "The '$name' field is required");
    });

    test('validateNotEmpty returns null if filled', () {
      final result = Validators.validateNotEmpty('Ma√Øva', 'Name');
      expect(result, null);
    });
  });
}
